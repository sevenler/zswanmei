<p class="MsoNormal" style="text-indent:27.0pt;mso-char-indent-count:2.0;
line-height:150%"><span style="font-size:13.5pt;line-height:150%;font-family:
华文仿宋;mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:0pt">要认识肽，需要从大家熟悉的<span lang="EN-US">&ldquo;</span>蛋白质<span lang="EN-US">&rdquo;</span>说起。蛋白质是什么？蛋白质构成和修复人体组织，通过酶、激素、抗体、遗传物质等形式调节机体生理功能，还能为人体提供能量。因此，蛋白质是一切生命活动的物质基础。<span lang="EN-US"><o:p></o:p></span></span></p>
<p class="MsoNormal" style="text-indent:27.0pt;mso-char-indent-count:2.0;
line-height:150%"><span style="font-size:13.5pt;line-height:150%;font-family:
华文仿宋;mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:0pt">氨基酸是蛋白质的基本构成单位。不同种类的氨基酸，按照特定的顺序，构成结构不同、功能各异的蛋白质。一般认为蛋白质是由<span lang="EN-US">51</span>个以上的氨基酸构成。而在生命体中，还有多种由<span lang="EN-US">2</span>～<span lang="EN-US">50</span>个氨基酸构成的物质，科学界将其称为<span lang="EN-US">&ldquo;</span>肽<span lang="EN-US">&rdquo;</span>。通常，将由<span lang="EN-US">2</span>～<span lang="EN-US">10</span>个氨基酸构成的称为低聚肽，将由<span lang="EN-US">11</span>～<span lang="EN-US">50</span>个氨基酸组成的称为多肽。在物质构成上，肽与蛋白质具有同质性，却具有不同氨基酸和蛋白质的独特的生理功能，尤其是低聚肽。<span lang="EN-US"><o:p></o:p></span></span></p>
<p class="MsoNormal" style="text-indent:27.0pt;mso-char-indent-count:2.0;
line-height:150%"><span style="font-size:13.5pt;line-height:150%;font-family:
华文仿宋;mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:0pt">传统上认为，蛋白质只有转变为氨基酸以后，才能被人体吸收和利用。现代研究发现，更多的蛋白质在胃肠道内转化为低聚肽，并以低聚肽的形式吸收。因此低聚肽对人体的生命活动和健康具有更为重要的意义。<span lang="EN-US"><o:p></o:p></span></span></p>
<p class="MsoNormal" style="text-indent:27.05pt;mso-char-indent-count:2.0;
line-height:150%"><b><span style="font-size:13.5pt;line-height:150%;font-family:
华文仿宋;mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:0pt">二、大豆低聚肽的理化和功能特性</span></b><span lang="EN-US" style="font-size:13.5pt;line-height:150%;font-family:华文仿宋;
mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:0pt"><o:p></o:p></span></p>
<p class="MsoNormal" style="text-indent:27.0pt;mso-char-indent-count:2.0;
line-height:150%"><span style="font-size:13.5pt;line-height:150%;font-family:
华文仿宋;mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:0pt">大豆低聚肽是将大豆蛋白通过酶解或酸水解的方法制备的小分子肽类物质，主要是由<span lang="EN-US">3</span>～<span lang="EN-US">6</span>个氨基酸构成，分子量在<span lang="EN-US">1000</span>以下。<span lang="EN-US"><o:p></o:p></span></span></p>
<p class="MsoNormal" style="text-indent:27.0pt;mso-char-indent-count:2.0;
line-height:150%"><span style="font-size:13.5pt;line-height:150%;font-family:
华文仿宋;mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:0pt">大豆中蛋白质含量丰富，并且具有平衡的氨基酸模式，是一类公认的植物优质蛋白质。众多的科学研究发现，将大豆蛋白转化为大豆低聚肽，使大豆低聚肽出现了大豆蛋白所不具备的多种特殊性。<span lang="EN-US"><o:p></o:p></span></span></p>
<p class="MsoNormal" style="text-indent:27.05pt;mso-char-indent-count:2.0;
line-height:150%"><b><span lang="EN-US" style="font-size:13.5pt;line-height:150%;
font-family:华文仿宋;mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:
0pt">1</span></b><b><span style="font-size:13.5pt;line-height:150%;font-family:
华文仿宋;mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:0pt">、大豆低聚肽的理化特性</span></b><span lang="EN-US" style="font-size:13.5pt;line-height:150%;font-family:华文仿宋;
mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:0pt"><o:p></o:p></span></p>
<p class="MsoNormal" style="text-indent:27.0pt;mso-char-indent-count:2.0;
line-height:150%"><span style="font-size:13.5pt;line-height:150%;font-family:
华文仿宋;mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:0pt">（<span lang="EN-US">1</span>）分子量小。大豆蛋白质的分子量高达数万或数十万。而大豆低聚肽的分子量平均在<span lang="EN-US">1000</span>以下。这样的低聚已经达到了能够直接被人体肠道吸收的水平。<span lang="EN-US"><o:p></o:p></span></span></p>
<p class="MsoNormal" style="text-indent:27.0pt;mso-char-indent-count:2.0;
line-height:150%"><span style="font-size:13.5pt;line-height:150%;font-family:
华文仿宋;mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:0pt">（<span lang="EN-US">2</span>）水溶性好。大豆蛋白溶解度低，而大豆低聚肽有很好的水溶性。水溶性往往决定营养物质被吸收利用的程度。<span lang="EN-US"><o:p></o:p></span></span></p>
<p class="MsoNormal" style="text-indent:27.0pt;mso-char-indent-count:2.0;
line-height:150%"><span style="font-size:13.5pt;line-height:150%;font-family:
华文仿宋;mso-bidi-font-family:Tahoma;color:#00B050;mso-font-kerning:0pt">（<span lang="EN-US">3</span>）热稳定性好。蛋白质遇热变性，而大豆低聚肽对热稳定。因此，可以利用大豆低聚肽加工制作多种形式的食品，不会造成其结构改变。<span lang="EN-US"><o:p></o:p></span></span></p>
<p class="MsoNormal" style="text-indent:27.0pt;mso-char-indent-count:2.0;
line-height:150%"><span style="font-size:13.5pt;line-height:150%;font-family:
