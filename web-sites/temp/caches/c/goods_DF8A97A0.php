<p class="MsoNormal" align="left" style="margin-bottom: 18pt; line-height: 18px; vertical-align: baseline;"><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">&nbsp; &nbsp; 黄褐斑也称肝斑，是发生于</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">面部的一种色素沉着性皮肤病。妇女妊娠，月经失调，盆腔疾病，外在</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">表现为前额，颧，鼻，上唇等处片状黄褐色或咖啡色斑，大小不一，边缘清</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">楚，表面光滑，无自觉症状。有时黄褐斑互相融合，形成蝴蝶状外观。</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<p class="MsoNormal" align="left" style="margin-bottom: 18pt; line-height: 18px; vertical-align: baseline;"><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">&nbsp; &nbsp; 雀斑的发生可能和遗传及曝晒日光有关。大多在</span></b><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">&nbsp;6-7</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">岁开始出现，好发在面部，有时颈和手背等处也可发生，为针头至绿豆大小</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">黄褐或淡黑色斑点，成群或疏散分布，表面光滑，无自觉症状。夏季经强烈日晒</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">后，颜色加深，数目增多，冬季则颜色转淡，数目也减少。</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<p class="MsoNormal" align="left" style="margin-bottom: 18pt; line-height: 18px; vertical-align: baseline;"><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">&nbsp; &nbsp; 黄褐斑，雀斑有先天就易出现的人，但是经过紫</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">外线的辐射后，更易产生。化妆品过敏，精神紧张，偏食，肝脏，肾脏等内脏机</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">能低下等原因，使体内黑色素生成后沉着于体内，不能顺利排出，都可能是造成</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">的原因。</span></b><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<p class="MsoNormal" align="left" style="margin-bottom: 18pt; line-height: 18px; vertical-align: baseline;"><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">&nbsp; &nbsp; 防晒和脱色是祛斑的关键，</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">芦荟胶有利于祛斑，这是因为：</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<p class="MsoNormal" align="left" style="margin-bottom: 18pt; line-height: 18px; vertical-align: baseline;"><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">&nbsp; &nbsp; 1.</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">芦荟中含有芦荟素，蒽醌，肉桂酸酯等成分都</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">对部分紫外线有吸收作用，能隔绝部分日晒。</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<p class="MsoNormal" align="left" style="margin-bottom: 18pt; line-height: 18px; vertical-align: baseline;"><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">&nbsp; &nbsp; 2.</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">芦荟胶中所含的氨基酸类，有机酸类，芦荟辛</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">等成分有抑制酪氨酸酶活性，阻碍黑色素形成的作用。</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<p class="MsoNormal" align="left" style="margin-bottom: 18pt; line-height: 18px; vertical-align: baseline;"><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">&nbsp; &nbsp; 3.</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">芦荟能增强微循环，促进细胞的新陈代谢，逐渐去除皮肤中沉着的色素。</span></b><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<p class="MsoNormal" align="left" style="margin-bottom: 18pt; line-height: 18px; vertical-align: baseline;"><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">&nbsp; &nbsp; 治疗方法：洗脸后，将芦荟胶与水按</span></b><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">1</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">：</span></b><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">3</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">的比例</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">稀释后均匀的抹到脸上，在有斑的部位多做按摩，以促进皮肤吸收，增强微循环</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">。</span></b><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">1</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">日</span></b><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">3</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">次，日间的</span></b><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">2</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">次不宜抹得太厚，而睡前的涂抹可厚一些。</span></b><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">&nbsp;&nbsp;</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<p class="MsoNormal" align="left" style="margin-bottom: 18pt; line-height: 18px; vertical-align: baseline;"><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">&nbsp; &nbsp; 说明：祛斑至少需要三个月才会显效，因为皮肤</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">中沉着的色素要通过细胞的新陈代谢才能脱掉，要耐心坚持治疗，另外也需查明</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">病因，去除病根才能治愈。</span></b><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<h2><b><span style="font-size: 27pt; line-height: 54px; font-family: 宋体; color: red;">（</span></b><b><span style="font-size: 27pt; line-height: 54px; font-family: 宋体; color: red;">三）青春痘</span></b><b><span lang="EN-US" style="font-size: 27pt; line-height: 54px; font-family: inherit, serif; color: red;">&nbsp;</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></h2>
<p class="MsoNormal" align="left" style="margin-bottom: 18pt; line-height: 18px; vertical-align: baseline;"><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">&nbsp; &nbsp; 概述：青春痘是一种常见于青年人的毛曩，皮脂腺的慢性炎症。有以下别称：痤疮、寻常痤疮、暗疮、酒刺、青春蕾、发蕾、发疙瘩、粉刺等。好发于青年男女面部和胸、背部。</span></b><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">&nbsp;&nbsp;</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<p class="MsoNormal" align="left" style="line-height: 18px; vertical-align: baseline;"><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">&nbsp; &nbsp; 病因：目前认为由内分泌旺</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">盛引起。体内的雄性激素（男女体内都有雄性激素）水平相对增高，使皮脂腺发</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">育旺盛，分泌增多；同时雄性激素又使毛曩，皮脂腺导管角化，栓塞，导致又多</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">又浓的皮脂不能完全排泄出去，逐渐积聚在毛曩口，加上毛曩壁上脱落的上皮细</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">胞，这样浓稠的皮脂就与脱落的细胞混合在一起，成为干酪样物质栓塞在毛曩口</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">内，就形成了痤疮（白头粉刺）。疮疱顶端部分逐渐干燥，又经过空气的氧化作</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">用及尘土的污染而变色，便形成常见的黑头粉刺，由于粉刺的挤压和局部游离脂</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">肪酸的侵入，引起毛曩，皮脂腺的炎症反应，出现丘疹，脓疱，结节，脓肿及瘢</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">痕等各种表现。</span></b></p>
<p class="MsoNormal" align="left" style="line-height: 18px; vertical-align: baseline;"><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">&nbsp; &nbsp; 方法：成熟的有脓包的豆豆，你可以用洗干净的手把脓包挤掉，然后再伤口上擦上芦荟胶，可以多用一点点。</span></b><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b></p>
<p class="MsoNormal" align="left" style="line-height: 18px; vertical-align: baseline;"><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b>&nbsp; &nbsp;&nbsp;</p>
<h2><b><span style="font-size: 24pt; line-height: 48px; font-family: 宋体; color: red;">（四）芦荟胶的美容用法</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></h2>
<p class="MsoNormal" align="left" style="line-height: 18px; vertical-align: baseline;"><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">&nbsp; &nbsp; 1</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">、</span></b><b><span style="font-size: 12pt; line-height: 24px; font-family: 宋体;">芦荟胶可以用来当面霜</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<p class="MsoNormal" align="left" style="margin-bottom: 18pt; line-height: 18px; vertical-align: baseline;"><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">&nbsp; &nbsp; 当面霜的话用量不能太多，多按膜让皮肤充分吸</span></b><b><span style="font-size: 13.5pt; line-height: 27px;">&nbsp;</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">收．如果用量太多，皮肤吸收不完，容易造成搓泥现象．因为芦荟多的时候可能有点粘的感觉．冬天擦了芦荟胶后保湿力度可能不够，会觉得不油腻，非常的清爽，用了非常舒服．</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<p class="MsoNormal" align="left" style="line-height: 18px; vertical-align: baseline;"><b><span lang="EN-US" style="font-size: 13.5pt; line-height: 27px;">&nbsp; &nbsp; 2</span></b><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">、</span></b><b><span style="font-size: 12pt; line-height: 24px; font-family: 宋体;">芦荟胶可以用来做面膜</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
<p class="MsoNormal" align="left" style="margin-bottom: 18pt; line-height: 18px; vertical-align: baseline;"><b><span style="font-size: 13.5pt; line-height: 27px; font-family: 宋体;">&nbsp; &nbsp; 有两种用法：</span></b><b><span lang="EN-US" style="font-size: 10pt; line-height: 19px; font-family: inherit, serif; color: rgb(102, 102, 102);"><o:p></o:p></span></b></p>
